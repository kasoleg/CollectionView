package com.lorempixel.presentation.adapter;

public class SpanSize {
    private final int mRows;
    private final int mColumns;

    public SpanSize(int rows, int columns) {
        this.mRows = rows;
        this.mColumns = columns;
    }

    public int getRows() {
        return mRows;
    }

    public int getColumns() {
        return mColumns;
    }


}
